package de.uniluebeck.itm.ncoap.application.server.webservice;


import java.util.HashSet;
import java.util.Iterator;
import java.util.Set;

/**
* Exception to {@link AcceptedContentFormatNotSupportedException} indicate that a requested content format is not provided by a
* {@link Webservice} instance.
*
* @author Oliver Kleine
*/
public class AcceptedContentFormatNotSupportedException extends Exception {

    private Set<Long> unsupportedContentFormats;

    /**
     * @param contentFormats the integer values representing the content formats that are not supported
     */
    public AcceptedContentFormatNotSupportedException(long... contentFormats){
        super();

        unsupportedContentFormats = new HashSet<>();
        for(long contentFormat : unsupportedContentFormats){
            this.unsupportedContentFormats.add(contentFormat);
        }
    }

    /**
     * Returns a (propably incomplete) {@link Set<Integer>} containing the content formats that caused the exception
     * @return a (propably incomplete) {@link Set<Integer>} containing the content formats that caused the exception
     */
    public Set<Long> getUnsupportedContentFormats() {
        return this.unsupportedContentFormats;
    }

    @Override
    public String getMessage(){
        return "Accepted Content Format(s) not supported: " + getUnsupportedContentFormatsAsString();
    }

    public String getUnsupportedContentFormatsAsString(){
        StringBuffer result = new StringBuffer();

        Iterator<Long> iterator = getUnsupportedContentFormats().iterator();
        while(iterator.hasNext()){
            result.append(iterator.next());

            if(iterator.hasNext())
                result.append(", ");
        }

        return result.toString();
    }
}
